export const generateMultilingualUrl = (baseUrl: string, language: string): string => {
  if (language === 'fr' || language === 'en') {
    return language === 'en' ? `/en${baseUrl}` : baseUrl;
  }
  
  return `/${language}${baseUrl}`;
};

export const getFooterLink = (linkType: string, language: string = 'fr'): string => {
  const langCode = language === 'ptBR' ? 'pt' : language;
  
  const urls: Record<string, Record<string, string>> = {
    fr: {
      support: '/help-center',
      faq: '/faq',
      helpCenter: '/help-center',
      contact: '/contact',
      terms: '/terms-of-service',
      privacy: '/privacy-policy',
      cookies: '/cookies-policy',
      legal: '/legal-notices'
    },
    en: {
      support: '/en/help-center',
      faq: '/en/faq',
      helpCenter: '/en/help-center',
      contact: '/en/contact',
      terms: '/en/terms-of-service',
      privacy: '/en/privacy-policy',
      cookies: '/en/cookies-policy',
      legal: '/en/legal-notices'
    }
  };
  
  return urls[langCode]?.[linkType] || urls.fr[linkType] || '/';
};

export const detectLinkTypeAndGenerateUrl = (linkName: string, href: string, language: string = 'fr'): string => {
  // Si c'est déjà une URL complète, la retourner
  if (href.startsWith('http') || href.startsWith('mailto:') || href.startsWith('tel:')) {
    return href;
  }
  
  // Si c'est un lien interne, générer l'URL multilingue
  if (href.startsWith('/')) {
    return generateMultilingualUrl(href, language);
  }
  
  // Sinon, utiliser la fonction getFooterLink
  return getFooterLink(href, language);
};