export const detectLinkTypeAndGenerateUrl = (linkName: string, href: string, language: string = 'fr'): string => {
  // Si c'est déjà une URL complète, la retourner
  if (href.startsWith('http') || href.startsWith('mailto:') || href.startsWith('tel:')) {
    return href;
  }
  
  // Si c'est un lien interne, générer l'URL multilingue
  if (href.startsWith('/')) {
    return generateMultilingualUrl(href, language);
  }
  
  // Sinon, utiliser la fonction getFooterLink
  return getFooterLink(href, language);
};